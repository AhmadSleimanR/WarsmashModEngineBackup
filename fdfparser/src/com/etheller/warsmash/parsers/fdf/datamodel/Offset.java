package com.etheller.warsmash.parsers.fdf.datamodel;

public class Offset {
	private float x;
	private float y;

	public float getX() {
		return this.x;
	}

	public float getY() {
		return this.y;
	}

	public void setX(final float x) {
		this.x = x;
	}

	public void setY(final float y) {
		this.y = y;
	}

}
